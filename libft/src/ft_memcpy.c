#include "libft.h"

void ft_memcpy(void *d1, const void *d2, size_t len)
{
	size_t i;

	i = 0;

	while(i < len)
	{
		*(char*) (d1 + i) = *(char*) (d2 + i);
		i++;
	}
	return(d1);
}
