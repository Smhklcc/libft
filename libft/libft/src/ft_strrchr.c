#include "libft.h"

char *ft_strrchar(const char *d, int l)
{
	int i;

	i = 0;
	while(d[i])
	{
		i++;
	}
	while(d[i] >= 0)
	{
		if (d[i] == (char)l)
			return ((char*)(d + i));
		i--;
	}
	return(NULL);
}
